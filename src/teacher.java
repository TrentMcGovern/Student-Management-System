    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int tId = Integer.parseInt(id.getText());
            String tName = name.getText();
            String tAddress = address.getText();
            String tPhone = phone.getText();
            String mail = email.getText();
            String salary = salar.getText();
            String department = depart.getText();
            String Hire = hired.getText();

            String sql = "UPDATE TEACHER SET name='"+tName+"',phone='"+tPhone+"',Address='"+tAddress+"',email='"+mail+"',HireDate='"+Hire+"',Salary='"+salary+"',department='"+department+"' WHERE id='"+tId+"'";

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data is successfully updated");

        }catch(HeadlessException | NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

