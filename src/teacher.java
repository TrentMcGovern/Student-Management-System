    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();

            int tId = Integer.parseInt(id.getText());

            String sql = "SELECT * FROM TEACHER WHERE id = '"+tId+"'";

            rs = stmt.executeQuery(sql);

            if(rs.next()){
                name.setText(rs.getString("name"));
                email.setText(rs.getString("email"));
                phone.setText(rs.getString("phone"));
                address.setText(rs.getString("Address"));
                salar.setText(rs.getString("Salary"));
                depart.setText(rs.getString("department"));
                hired.setText(rs.getString("HireDate"));
            }else{
                JOptionPane.showMessageDialog(null, "Record Not Found");
            }

        }catch(HeadlessException | NumberFormatException | SQLException e){JOptionPane.showMessageDialog(null,e);}

    }//GEN-LAST:event_jButton2ActionPerformed

