    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        try{
                stmt = conn.createStatement();
                
                int Cid = Integer.parseInt(id.getText());
                
                String sql = "SELECT * FROM COURSE WHERE courseId = '"+Cid+"'";
                
                rs = stmt.executeQuery(sql);
                
                if(rs.next()){
                    courseName.setText(rs.getString("name"));
                    day.setText(rs.getString("days"));
                    duration.setText(rs.getString("duration"));
                    credits.setText(String.format("%s",rs.getInt("credits")));
                    time.setText(rs.getString("timing"));
                    teacher.setText(rs.getString("teacher"));
                    description.setText(rs.getString("description"));
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Record Not Found");
                }
                
            }catch(HeadlessException | NumberFormatException | SQLException e){JOptionPane.showMessageDialog(null,e);}
    
        

    }//GEN-LAST:event_searchButtonActionPerformed

